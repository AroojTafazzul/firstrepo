  // Retrieve the number of decimals of a given currency
  function fncGetCurrencyDecNo(strCurCode)
  {
    // If the currency is empty (typically when an amount field is populated
    // before its currency), we set by default the decimal part to 2 digits
    if ("S" + strCurCode == "S")
     return 2;

    for (i = 1; i < arrCurrency.length; i++) 
    {
      if (arrCurrency[i][1] == strCurCode) 
        return arrCurrency[i][3];
    }
    return -1
  }

  // Check that the currency passed as a parameter is valid
  function fncCheckValidCurrency(objCurCode)
  {
    // If the currency string is empty, we return true
    if ("S" + objCurCode.value == "S")
      return true;

    objCurCode.value = objCurCode.value.toUpperCase();
    for (i = 1; i < arrCurrency.length; i++) 
    {
      if (arrCurrency[i][1] == objCurCode.value) 
        return true;
    }
    fncShowError(1,objCurCode.value);
    objCurCode.value = "";
    return false;
  }
